void KsiazkaAdresowa::wylogowanieUzytkownika()
{
    uzytkownikMenedzer.wylogowanieUzytkownika();
    delete adresatMenedzer;//bardzo wazne aby po przypisaniu 0 do idZalogowanegoUzytkownika
    //w klasie UzytkownikMenedzer usunac w klasie KsizkaAdresowa to na co wksazuje
    //wkaznik adresatMenedzer. Unikniemy wyciekow pamieci
    adresatMenedzer = NULL;
}

void KsiazkaAdresowa::dodajAdresata()
{
if (uzytkownikMenedzer.czyUzytkownikJestZalogowany())
    {
        adresatMenedzer->dodajAdresata();
    }
    else {
        cout << "Najpierw musisz sie zalogowac" << endl;
        system ("pause");
    }
}

